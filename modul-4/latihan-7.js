// Contoh sederhana penggunaan if, if else, dan else dalam JavaScript

let angka = 7;

// Menggunakan if
if (angka > 5) {
  console.log("Angka lebih besar dari 5");
}

// Menggunakan if else
if (angka % 2 === 0) {
  console.log("Angka genap");
} else {
  console.log("Angka ganjil");
}

// Menggunakan if else if else
if (angka > 10) {
  console.log("Angka lebih besar dari 10");
} else if (angka > 5) {
  console.log("Angka lebih besar dari 5 tapi tidak lebih dari 10");
} else {
  console.log("Angka 5 atau kurang");
}
