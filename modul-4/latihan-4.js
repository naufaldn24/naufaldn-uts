// Angka bulat (integer)
let angkaBulat = 25;
console.log(angkaBulat); // Output: 25

// Angka desimal (float)
let angkaDesimal = 3.14;
console.log(angkaDesimal); // Output: 3.14

// Penjumlahan antara angka bulat dan desimal
let total = angkaBulat + angkaDesimal;
console.log(total); // Output: 28.14

// Perkalian angka bulat dan desimal
let hasilKali = angkaBulat * angkaDesimal;
console.log(hasilKali); // Output: 78.5

// Penggunaan variabel dengan tipe data angka untuk operasi matematika
let pembagian = 10 / 2;
console.log(pembagian); // Output: 5
